.MODEL SMALL

.STACK 100H

.DATA
	FILENAME DB 'TESTFILE.TXT'
	HANDLE 	 DW ?
.CODE
BEGIN:
	MOV AX, @DATA		; INITIALISE
	MOV DS, AX
	
	LEA DX, FILENAME	; CREATE FILE
	MOV CX, 0H
	MOV AH, 3CH
	INT 21H
	MOV HANDLE, AX

	MOV BX, HANDLE		; CLOSE THE FILE
	MOV AH, 3EH
	INT 21H

	MOV AH, 4CH		; RETURN
	INT 21H
END BEGIN
