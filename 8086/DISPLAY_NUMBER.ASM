.MODEL SMALL

.STACK 100H

.DATA

.CODE
BEGIN:	MOV AX, @DATA
	MOV DS, AX
	
	MOV AX, 1234H		; NUMBER TO BE DISPLAYED
	CALL DISP

	MOV AH, 4CH
	INT 21H

DISP	PROC
	MOV BX, 10H
	MOV DX, 0000H
	MOV CX, 0000H

P1:	MOV DX, 0000H
	DIV BX
	PUSH DX
	INC CX
	CMP AX, 0H
	JNE P1

P2:	POP DX
	ADD DX, 30H
	MOV AH, 02H
	INT 21H
	LOOP P2
	RET
DISP	ENDP

END BEGIN
