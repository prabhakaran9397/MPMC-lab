.MODEL SMALL
.STACK 100H
.DATA
	STRING	DB 50H, ?, 50H DUP('$')
	REVSTR	DB 50H DUP('$')
	NL	DB 0AH, '$'
.CODE
BEGIN:
	MOV AX, @DATA			; INITIALISE
	MOV DS, AX			

	MOV DX, OFFSET STRING		; SCAN		
	MOV AH, 0AH			
	INT 21H		

	MOV SI, OFFSET STRING+1		; LENGTH
	MOV CL, [SI]
	MOV CH, 0H
	INC CX
	MOV AL, '$'			; PUTTING $ AT START
	MOV [SI], AL
	ADD SI, CX
	MOV [SI], AL 			; PUTTING $ AT END

	MOV SI, OFFSET STRING+1		; REVERSING
	DEC CX
	ADD SI, CX
	LEA DI, REVSTR

COPY:	MOV BL, [SI]
	MOV [DI], BL 
	INC DI
	DEC SI
	CMP BL, AL
	JNE COPY

	MOV DX, OFFSET NL		; PRINT NEWLINE
	MOV AH, 09H					
	INT 21H

	MOV DX, OFFSET STRING+2		; PRINT STRING
	MOV AH, 09H					
	INT 21H

	MOV DX, OFFSET NL		; PRINT NEWLINE
	MOV AH, 09H					
	INT 21H

	MOV DX, OFFSET REVSTR		; PRINT REVSTR
	MOV AH, 09H					
	INT 21H

	MOV AH, 4CH			; RETURN
	INT 21H				
END BEGIN

