.MODEL SMALL

.STACK 100H

.DATA
	STRING	DB 80 DUP('$')
	STRSTR	DB 40 DUP('$')
	NL	DB 0DH, 0AH, '$'
	PROMPT1	DB 'ENTER THE STRING: $'
	PROMPT2	DB 'ENTER THE STRSTR: $'
	PROMPT3	DB 'YES! FOUND$'
	PROMPT4	DB 'NO! NOT FOUND$'
.CODE
BEGIN:	MOV AX, @DATA
	MOV DS, AX

	LEA DX, PROMPT1
	MOV AH, 09H
	INT 21H

	LEA DX, STRING
	MOV AH, 0AH
	INT 21H
	LEA SI, STRING+1
	MOV CL, [SI]
	MOV CH, 0H
	MOV AL, '$'
	INC CX
	ADD SI, CX
	MOV [SI], AL	

	LEA DX, NL
	MOV AH, 09H
	INT 21H

	LEA DX, PROMPT2
	MOV AH, 09H
	INT 21H

	LEA DX, STRSTR
	MOV AH, 0AH
	INT 21H
	LEA SI, STRSTR+1
	MOV CL, [SI]
	MOV CH, 0H
	MOV AL, '$'
	INC CX
	ADD SI, CX
	MOV [SI], AL

	LEA DX, NL
	MOV AH, 09H
	INT 21H

	MOV AL, '$'
	LEA SI, STRING+2
SUBS:	LEA DI, STRSTR+2
COMP:	CMP [DI], AL
	JE SUCC
	CMP [SI], AL
	JE FAIL
	MOV BL, [SI]
	CMP BL, [DI]
	JE INCR
	INC SI	
	JMP SUBS

INCR:	INC SI
	INC DI
	JMP COMP	

SUCC:	LEA DX, PROMPT3
	MOV AH, 09H
	INT 21H
	JMP LAST

FAIL:	LEA DX, PROMPT4
	MOV AH, 09H
	INT 21H

LAST:	MOV AH, 4CH
	INT 21H	
END BEGIN
